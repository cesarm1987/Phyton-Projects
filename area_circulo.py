PI = 3.14
radio = float(input(' Por favor introduzca el radio del circulo: '))

diametro = 2 * radio
Circunferencia = 2 * PI * radio
area = PI * radio * radio

print(" \nDiametro de un circulo = %.2f" %diametro)
print(" Circunferencia de un circulo = %.2f" %Circunferencia)
print(" Area de un circulo = %.2f" %area)